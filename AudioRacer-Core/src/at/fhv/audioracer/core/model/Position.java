package at.fhv.audioracer.core.model;

public class Position {
	private float _posX;
	private float _posY;
	
	public float getPosX() {
		return _posX;
	}
	public void setPosX(float posX) {
		this._posX = posX;
	}
	public float getPosY() {
		return _posY;
	}
	public void setPosY(float posY) {
		this._posY = posY;
	}
}
